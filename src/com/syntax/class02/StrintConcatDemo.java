package com.syntax.class02;

public class StrintConcatDemo {

	public static void main(String[] args) {
		// Some of String data type examples

		String firstname = "Sergey";
		String lastname = "Shevchenko";
		long phnumber = 380684058883L;
		String address = "Kyiv, Ukraine";
		String status = "married";

		System.out.println("First Name" + ": " + firstname);
		System.out.println("Last Name" + ": " + lastname);
		System.out.println("Phone Number" + ": " + phnumber);
		System.out.println("Address" + ": " + address);
		System.out.println("Status" + ": " + status);

		boolean is = true;
		System.out.println("Live in Kyiv: " + is);
	}

}
